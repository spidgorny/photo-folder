import Head from "next/head";
import useSWR from "swr";
import Preview, { PreviewComponent } from "./preview";
import { PropsWithChildren } from "react";
import Image, { ImageProps } from "next/image";

// @ts-ignore
export const fetcher = (...args) => fetch(...args).then((res) => res.json());

export function PreviewImage(props: PropsWithChildren<{ src: string }>) {
  return (
    <div style={{ border: "solid 1px silver" }}>
      <Image src={props.src} width={320} height={200} />
    </div>
  );
}

export function ListFiles() {
  const { data, error } = useSWR("/api/files", fetcher);
  if (error) return <div>{error}</div>;
  if (!data) return <div>loading...</div>;
  return (
    <div id="ListFiles" className="d-flex flex-wrap">
      {data.files.map((file: string) => (
        <PreviewImage
          key={file}
          src={"/api/readfile/" + encodeURIComponent(file)}
        >
          {file}
        </PreviewImage>
      ))}
    </div>
  );
}

export default function Home() {
  return (
    <div>
      <Head>
        <title>Photo Folder</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
        <link
          href="https://cdn.jsdelivr.net/npm/bootstrap@5.0.1/dist/css/bootstrap.min.css"
          rel="stylesheet"
          integrity="sha384-+0n0xVW2eSR5OomGNYDnhzAbDsOXxcvSN1TPprVMTNDbiYZCxYbOOl7+AMvyTG2x"
          crossOrigin="anonymous"
        />
      </Head>

      <main className="container">
        <ListFiles />
      </main>
    </div>
  );
}
